package engine.actions;

public class FieldsActions {

    // Text Field :
    // - Text Field -> Single Line Text	For short strings (e.g., names, titles).
    // - Text Area ->  Multi-line Text	For longer input (e.g., descriptions, comments).
    // - Password Field	-> Obscured input for passwords.
    // - Email Field -> For valid email addresses (often with validation).
    // - Search Field -> For searching text (often with auto-suggestions).

    // Numeric Fields :
    // - Number Field	-> For numeric input (e.g., integers, decimals).
    // - Decimal Field -> For decimal numbers with specific precision.
    // - Currency Field	-> For currency values (often with currency symbol).
    // - Range / Slider Field	Select a numeric value from a range using a slider.

    // Date & Time Fields:
    // - Date Picker	-> For selecting a specific date.
    // - Time Picker	-> For selecting a time (e.g., 14:30).
    // - DateTime Picker	-> For selecting both date and time.
    // - Month Picker	-> For selecting a month/year only.
    // - Week Picker	-> For selecting a specific week.
    // - Duration Field	-> Represents a time span (e.g., 2h 30m).

    // Boolean & Choice Fields:
    // - Checkbox	-> For binary yes/no or multiple selections.
    // - Radio Buttons	-> For single choice from multiple options.
    // - Switch -> A binary on/off control.
    // - Dropdown / Select -> For choosing from a list of options.
    // - Multi-Select Dropdown	-> Choose multiple options from a list.

    // File & Media Fields:
    //    File Upload	-> For uploading files.
    //    Image Upload	-> Specifically for images.

    //  Specialized Fields:
    //    Color Picker	-> For selecting a color.
    //    Phone Number	-> Often includes country code validation.
    //    Geolocation / Map Picker	-> For selecting coordinates or addresses.
    //    Captcha / reCAPTCHA	-> Used to verify the user is human.
    //    Barcode / QR Code Scanner	-> Input from a scanner or camera.
    //    Signature Field	-> Capture a digital signature.
}
